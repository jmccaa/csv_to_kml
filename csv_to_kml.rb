  def initialize(min,max,mapname=PREDEFINED_MAPS.keys.sort.first)
    raise "don't know about map named #{mapname}" unless PREDEFINED_MAPS.include?(mapname)
    @min = min.to_f
    @max = max.to_f
    @map = []
    PREDEFINED_MAPS[mapname].each_with_index{|value,i|
      @map << Array.new(4) if i.remainder(3)==0
      @map.last[i.remainder(3)+1] = sprintf("%02x",value)
    }
    @map.reverse!
    num_entries = @map.size
    num_entries.times{|i| @map[i][0] = i.to_f/(num_entries-1)}
  end
  
  def color_at(value)
#    raise "value #{value} must be between #{@min} and #{@max}, inclusive" if value < @min or value > @max
    normalized_value = ([[value.to_f,@min].max,@max].min - @min) / (@max - @min)
    @map.each{|entry|
      return entry[1..3].join("") if entry[0] >= normalized_value
    }
    raise "failed to find #{value} between #{@min} and #{max}"
  end

